//! A 5×7 pixel font, drawn as filled cells.
//!
//! macroquad's own TrueType text is rasterized at the logical resolution and
//! then magnified when the canvas is scaled to the window, so its antialiased
//! edges go soft. Drawing letters as hard-edged pixel blocks keeps text as crisp
//! as the rest of the field under nearest-neighbour scaling, and matches the
//! era's look.

use macroquad::prelude::*;

const GLYPH_W: usize = 5;
const GLYPH_H: usize = 7;
/// One blank cell between glyphs.
const GAP: usize = 1;

/// The width, in logical units, of `text` drawn at `scale`.
pub fn text_width(text: &str, scale: f32) -> f32 {
    let cells = text.chars().count();
    if cells == 0 {
        return 0.0;
    }
    ((cells * GLYPH_W + (cells - 1) * GAP) as f32) * scale
}

/// Draws `text` with its top-left at `(x, y)`, each pixel a `scale`×`scale`
/// block. Positions are snapped to whole logical units so the blocks land on
/// exact pixels and stay crisp when the canvas is scaled up.
pub fn draw(text: &str, x: f32, y: f32, scale: f32, colour: Color) {
    let top = y.round();
    let mut cell_x = x.round();
    for ch in text.chars() {
        let rows = glyph(ch.to_ascii_uppercase());
        for (row, bits) in rows.iter().enumerate() {
            for col in 0..GLYPH_W {
                let lit = bits & (1 << (GLYPH_W - 1 - col)) != 0;
                if lit {
                    draw_rectangle(
                        cell_x + col as f32 * scale,
                        top + row as f32 * scale,
                        scale,
                        scale,
                        colour,
                    );
                }
            }
        }
        cell_x += (GLYPH_W + GAP) as f32 * scale;
    }
}

/// Draws `text` centred across a field `logical_width` wide, with `y` as its top
/// edge.
pub fn draw_centred(logical_width: f32, text: &str, y: f32, scale: f32, colour: Color) {
    let x = (logical_width - text_width(text, scale)) / 2.0;
    draw(text, x, y, scale, colour);
}

/// The seven rows of a glyph, top to bottom. The low five bits of each row are
/// its pixels, most-significant bit leftmost. Anything unknown draws blank.
fn glyph(ch: char) -> [u8; GLYPH_H] {
    match ch {
        'A' => [0x0E, 0x11, 0x11, 0x1F, 0x11, 0x11, 0x11],
        'B' => [0x1E, 0x11, 0x11, 0x1E, 0x11, 0x11, 0x1E],
        'C' => [0x0E, 0x11, 0x10, 0x10, 0x10, 0x11, 0x0E],
        'D' => [0x1E, 0x11, 0x11, 0x11, 0x11, 0x11, 0x1E],
        'E' => [0x1F, 0x10, 0x10, 0x1E, 0x10, 0x10, 0x1F],
        'F' => [0x1F, 0x10, 0x10, 0x1E, 0x10, 0x10, 0x10],
        'G' => [0x0E, 0x11, 0x10, 0x17, 0x11, 0x11, 0x0F],
        'H' => [0x11, 0x11, 0x11, 0x1F, 0x11, 0x11, 0x11],
        'I' => [0x1F, 0x04, 0x04, 0x04, 0x04, 0x04, 0x1F],
        'J' => [0x07, 0x02, 0x02, 0x02, 0x02, 0x12, 0x0C],
        'K' => [0x11, 0x12, 0x14, 0x18, 0x14, 0x12, 0x11],
        'L' => [0x10, 0x10, 0x10, 0x10, 0x10, 0x10, 0x1F],
        'M' => [0x11, 0x1B, 0x15, 0x15, 0x11, 0x11, 0x11],
        'N' => [0x11, 0x11, 0x19, 0x15, 0x13, 0x11, 0x11],
        'O' => [0x0E, 0x11, 0x11, 0x11, 0x11, 0x11, 0x0E],
        'P' => [0x1E, 0x11, 0x11, 0x1E, 0x10, 0x10, 0x10],
        'Q' => [0x0E, 0x11, 0x11, 0x11, 0x15, 0x12, 0x0D],
        'R' => [0x1E, 0x11, 0x11, 0x1E, 0x14, 0x12, 0x11],
        'S' => [0x0F, 0x10, 0x10, 0x0E, 0x01, 0x01, 0x1E],
        'T' => [0x1F, 0x04, 0x04, 0x04, 0x04, 0x04, 0x04],
        'U' => [0x11, 0x11, 0x11, 0x11, 0x11, 0x11, 0x0E],
        'V' => [0x11, 0x11, 0x11, 0x11, 0x11, 0x0A, 0x04],
        'W' => [0x11, 0x11, 0x11, 0x15, 0x15, 0x15, 0x0A],
        'X' => [0x11, 0x11, 0x0A, 0x04, 0x0A, 0x11, 0x11],
        'Y' => [0x11, 0x11, 0x0A, 0x04, 0x04, 0x04, 0x04],
        'Z' => [0x1F, 0x01, 0x02, 0x04, 0x08, 0x10, 0x1F],
        '0' => [0x0E, 0x11, 0x13, 0x15, 0x19, 0x11, 0x0E],
        '1' => [0x04, 0x0C, 0x04, 0x04, 0x04, 0x04, 0x0E],
        '2' => [0x0E, 0x11, 0x01, 0x06, 0x08, 0x10, 0x1F],
        '3' => [0x1F, 0x02, 0x04, 0x02, 0x01, 0x11, 0x0E],
        '4' => [0x02, 0x06, 0x0A, 0x12, 0x1F, 0x02, 0x02],
        '5' => [0x1F, 0x10, 0x1E, 0x01, 0x01, 0x11, 0x0E],
        '6' => [0x06, 0x08, 0x10, 0x1E, 0x11, 0x11, 0x0E],
        '7' => [0x1F, 0x01, 0x02, 0x04, 0x08, 0x08, 0x08],
        '8' => [0x0E, 0x11, 0x11, 0x0E, 0x11, 0x11, 0x0E],
        '9' => [0x0E, 0x11, 0x11, 0x0F, 0x01, 0x02, 0x0C],
        '(' => [0x02, 0x04, 0x08, 0x08, 0x08, 0x04, 0x02],
        ')' => [0x08, 0x04, 0x02, 0x02, 0x02, 0x04, 0x08],
        '>' => [0x10, 0x08, 0x04, 0x02, 0x04, 0x08, 0x10],
        '-' => [0x00, 0x00, 0x00, 0x1F, 0x00, 0x00, 0x00],
        _ => [0x00; GLYPH_H],
    }
}
